package de.bitkings.jbrotli;

public class BrotliDe_compressException extends RuntimeException {
  public BrotliDe_compressException() {
  }

  public BrotliDe_compressException(String message) {
    super(message);
  }

  public BrotliDe_compressException(String message, Throwable cause) {
    super(message, cause);
  }

  public BrotliDe_compressException(Throwable cause) {
    super(cause);
  }

  public BrotliDe_compressException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }
}
